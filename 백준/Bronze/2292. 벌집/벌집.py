# 백준 2292번 - 벌집
"""
문제 설명:
위의 그림과 같이 육각형으로 이루어진 벌집이 있다.
중앙의 방 1부터 시작해서 이웃하는 방에 돌아가면서 1씩 증가하는 번호를 주소로 매길 수 있다.
숫자 N이 주어졌을 때, 벌집의 중앙 1에서 N번 방까지 최소 개수의 방을 지나서 갈 때
몇 개의 방을 지나가는지(시작과 끝을 포함하여)를 계산하는 프로그램을 작성하시오.
"""

# 입력 받기
N = int(input())

# 벌집의 구조 패턴 분석:
# 1번 방 주변에는 6개의 방이 있고, 
# 그 다음 테두리에는 12개의 방이 있으며,
# 그 다음 테두리에는 18개의 방이 있다.
# 즉, 테두리를 이루는 방의 개수는 6의 배수로 증가한다 (6, 12, 18, 24, ...)

count = 1  # 지나는 방의 개수 (시작 방 포함)
room_num = 1  # 현재 방 번호 (시작은 1번 방)

# N이 1인 경우는 특별 케이스
if N == 1:
    print(1)
else:
    # N이 1보다 큰 경우
    while True:
        room_num += 6 * count  # 다음 테두리의 마지막 방 번호
        count += 1
        if N <= room_num:
            print(count)
            break